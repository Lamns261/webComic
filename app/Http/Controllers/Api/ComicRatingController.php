    }

    public function getRatingOfComics($comic_slug)
    {
        $ratings = Rating::query();

        $ratings->whereHas('comic', function ($query) use ($comic_slug) {
            $query->where('comic_slug', $comic_slug);
        });

        return $ratings->orderBy('id', 'desc')->get();
    }
